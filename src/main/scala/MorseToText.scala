package Morse
object Morse extends App {
   def MorseToTextTest(input: String):String={
     val morse = Array(".-","-...","-.-.", "-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--..",".-.-.-","--..--","---...",".-..-.",".----.","-.-.--","..--..",".--.-.","-....-","-.-.-.","-.--.","-.--.-","-...-",".----","..---", "...--","....-",".....","-....","--...","---..","----.","-----")
     val alphabet =  Array("a","b","c","d","e","f","g","h","i","j","k","l","m","n","o","p","q","r","s","t","u","v","w","x","y","z",".",",",":","","'","!","?","@","-", ";", "(",")","=","1","2","3","4","5","6","7","8","9","0")

     val inputSplit = input.split(" ");
     var text = "";
     var i = 0;
     var j = 0;

     for (i <- 0 until inputSplit.length){
       for(j <- 0 until morse.length){
         if(inputSplit(i)== morse(j)){
           text += alphabet(j)
         }
       }
     } 
     return text
  }
}
  